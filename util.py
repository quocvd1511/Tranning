def PrintInfo(text):
    print(f"[*] {text}.")


def PrintError(text):
    print(f"[!] {text}.")


def PrintExecuting(text):
    print(f"[.] {text}.")


def PrintSuccess(text):
    print(f"[+] {text}.")


def PrintFail(text):
    print(f"[-] {text}.")


